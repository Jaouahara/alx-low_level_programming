#include "3-calc.h"

/**
 * op_add - adds 2 integers
 * @a: the first int
 * @b: the second int
 *
 * Return: SUM
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts 2 integers
 * @a: first int
 * @b: second int
 *
 * Return: Difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mod - modulo 2 integers
 * @a: first int
 * @b: second int
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_div - divides 2 integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mul - multiplication of 2 integers
 * @a: first int
 * @b: second int
 *
 * Return: the product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
